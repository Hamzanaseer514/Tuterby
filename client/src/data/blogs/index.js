let imageIndex = 0;

export const blogData = allBlogImports.reduce((acc, blogModule) => {
  if (!blogModule) {
    console.warn("Found an undefined blog module in allBlogImports. Skipping.");
    return acc;
  }
  const slug = Object.keys(blogModule)[0];
   if (!slug) {
    console.warn("Found a blog module with no slug. Skipping:", blogModule);
    return acc;
  }
  const blogContent = blogModule[slug];
  
  if (!blogContent) {
    console.warn(`Blog content for slug '${slug}' is undefined. Skipping.`);
    return acc;
  }

  let currentImage = blogContent.mainImageSrc || blogContent.image;

  if (!currentImage || currentImage === "placeholder") {
    currentImage = defaultImages[imageIndex % defaultImages.length];
    imageIndex++;
  }
  
  acc[slug] = { 
    ...blogContent, 
    slug: slug,
    mainImageSrc: currentImage, 
    image: currentImage 
  };
  return acc;
}, {});